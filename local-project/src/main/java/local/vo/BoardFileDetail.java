package local.vo;

public class BoardFileDetail extends BoardFile{
	private int fileOrd;
	private int fileId;
	private String fileRealNm;
	private String fileOriginNm;
	private String fileThumbnailNm;
	private String fileExtention;
	private int fileSize;
	
	
	public int getFileOrd() {
		return fileOrd;
	}
	public void setFileOrd(int fileOrd) {
		this.fileOrd = fileOrd;
	}
	public int getFileId() {
		return fileId;
	}
	public void setFileId(int fileId) {
		this.fileId = fileId;
	}
	public String getFileRealNm() {
		return fileRealNm;
	}
	public void setFileRealNm(String fileRealNm) {
		this.fileRealNm = fileRealNm;
	}
	public String getFileOriginNm() {
		return fileOriginNm;
	}
	public void setFileOriginNm(String fileOriginNm) {
		this.fileOriginNm = fileOriginNm;
	}
	public String getFileThumbnailNm() {
		return fileThumbnailNm;
	}
	public void setFileThumbnailNm(String fileThumbnailNm) {
		this.fileThumbnailNm = fileThumbnailNm;
	}
	public String getFileExtention() {
		return fileExtention;
	}
	public void setFileExtention(String fileExtention) {
		this.fileExtention = fileExtention;
	}
	public int getFileSize() {
		return fileSize;
	}
	public void setFileSize(int fileSize) {
		this.fileSize = fileSize;
	}
	
	
	
	
}
